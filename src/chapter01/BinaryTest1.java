package chapter01;

public class BinaryTest1 {
	//Class명은 반드시 대문자로 시작해야 함
	public static void main(String[] args) {
		//자료구조 = type 
		 byte aVar = 120;
		 short bVar = 30000;
		 int cVar = 50000;
		 long dVar = 1000000;

		 
		 System.out.println("byte 타입 : " + aVar);
		 //줄 바꿔서 결과 출력
		 System.out.print("short 타입 : " + bVar);
		 //줄 안바꾸고 결과 출력
		 System.out.println(" / int 타입 : " + cVar);
		 System.out.println("long 타입 : " + dVar);

	}

}

